var oppositionApplicationLogoDeviceVar = document.getElementById("oppositionApplicationLogoDeviceBlock");
var oppositionApplicationDateOfUsingVar = document.getElementById("oppositionApplicationDateOfUsingBlock");
var oppositionOurTrademarkDateOfUsingVar = document.getElementById("oppositionOurTrademarkDateOfUsingBlock");
var oppositionOurTrademarkLogoDeviceVar = document.getElementById("oppositionOurTrademarkLogoDeviceBlock");

// To Make opposition Application Logo/Device Visible

function oppositionApplicationLogoDeviceVisible() {
    if(oppositionApplicationLogoDeviceVar.classList.contains('visible'))
    {
        //Do Nothing
        oppositionApplicationLogoDeviceVar.classList.remove("notvisible");
    }
    else
    {
        oppositionApplicationLogoDeviceVar.classList.remove("notvisible");
        oppositionApplicationLogoDeviceVar.classList.add("visible");
    }
}


function oppositionApplicationLogoDeviceNotVisible() {
    if(oppositionApplicationLogoDeviceVar.classList.contains('notvisible'))
    {
        //Do Nothing
        oppositionApplicationLogoDeviceVar.classList.remove("visible");
    }
    else
    {
        oppositionApplicationLogoDeviceVar.classList.remove("visible");
        oppositionApplicationLogoDeviceVar.classList.add("notvisible");
    }
}


